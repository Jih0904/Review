package r0612;

public class C01Rational {

	public static void main(String[] args) {
		//비교관계 연산자
		int num1 = 10;
		int num2 = 5;
		
		System.out.println(num1 == num1); //==는 같다 , =는 저장
		System.out.println(num1 == num2);
		System.out.println(num1 != num2); //!= 같지않다
		System.out.println(num1 > num2);
		System.out.println(num1 < num2);
		System.out.println(num1 >= num2);
		System.out.println(num1 <= num2); //부등호 순서 잘 맞추기
		


	}

}
